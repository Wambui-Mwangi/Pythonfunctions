def add(a,b):
    answer = a+b
    return answer

def subtract (c,d):
    result = c-d
    return result

def divide(e,f):
    quotient = e/f
    return quotient

def multiply(num1, num2):
    product = num1*num2
    return product

def remainder(num3, num4):
    modulus = num3%num4
    return modulus





